var A = parseInt(prompt("Digite o valor de A"));
var B = parseInt(prompt("Digite o valor de B"));
var C = parseInt(prompt("Digite o valor de C"));
var D = parseInt(prompt("Digite o valor de D"));

// Operações de soma
var somaAB = A + B;
var somaAC = A + C;
var somaAD = A + D;
var somaBC = B + C;
var somaBD = B + D;
var somaCD = C + D;
