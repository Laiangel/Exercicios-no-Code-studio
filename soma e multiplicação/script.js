var multAC = A * C;
var multAD = A * D;
var multBC = B * C;
var multBD = B * D;
var multCD = C * D;
alert("Soma de A + B = " + somaAB);
alert("Soma de A + C = " + somaAC);
alert("Soma de A + D = " + somaAD);
